'''ingresar la carga de dos nombres por teclado y mostrar cual de los dos es mayor alfabeticamente'''

nombre1 = input("Ingrese el primero nombre :")
nombre2 = input("Ingrese el segundo nombre :")
if nombre1 > nombre2:
    print(f"{nombre1 } es mayor alfabaticamente")
else:
    print (f"{nombre2} es mayor alfabeticamente")
