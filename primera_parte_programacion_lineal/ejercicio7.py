'''calcular el sueldo mensual de un operario conociendo la cantidad de horas trabajadas
y el valor por hora'''


horas = int (input("ingrese la cantidad de horas trabajadas :"))
valor_hora = int (input (" ingrese el valor de la hora trabajada :"))
sueldo = horas * valor_hora
print (" el sueldo del trabajador es :")
print (sueldo)